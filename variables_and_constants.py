# variable
'''
rules for naming variables
1. A variable name must start with a letter or the underscore character
2. A variable name cannot start with a number
3. A variable name can only contain alpha-numeric characters and underscores (A-z, 0-9, and _ )
4. Variable names are case-sensitive (name, Name, and NAME are three different variables)
'''
name = "John"
print(name) # John

# multiple assignment
x, y, z = 1, 2, 3
print(x) # 1
print(y) # 2
print(z) # 3


# constant
'''
constants are written in uppercase letters
(However, this is not mandatory, but it is a common practice to differentiate constants from variables)
'''
PI = 3.14
print(PI) # 3.14
