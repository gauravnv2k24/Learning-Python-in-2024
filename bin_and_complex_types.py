# complex number
# numbers in python can be real or complex
a = 1 + 2j
b = 3 + 4j
print(a + b) # (4+6j)


# bin() function
# bin() function returns the binary representation of a number
# bin(x) returns the binary representation of x
# x must be an integer

print(bin(10)) # 0b1010 => 1010 is the binary representation of 10

# convert binary to decimal
print(int('0b1010', 2)) # 10