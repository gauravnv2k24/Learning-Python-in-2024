
# to escape a single character use \
weather = "It\'s \"kind of\" sunny"
print(weather) # It's "kind of" sunny

# to add a tab space use \t
# note: 1 tab = 4 spaces
weather = "It\'s \"kind of\" \tsunny"
print(weather) # It's "kind of"  sunny

# to add a new line use \n
weather = "It\'s \"kind of\" \nsunny"
print(weather) 
# It's "kind of"  
# sunny
